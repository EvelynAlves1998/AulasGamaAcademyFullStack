/* A Estrutura de dados Árvore
É uma estrutura de dados que organiza seus elementos de forma hierárquica, 
onde existe um elemento que fica no topo da árvore, chamado de raiz 
e existem os elementos subordinados a ele, que são chamados de nós filhos. 
Cada nós filho pode conter zero, um ou mais de um nós filhos. */